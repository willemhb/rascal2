#ifndef rctypes_h

/* begin rctypes.h */
#define rctypes_h
#include <stdint.h>
#include <uchar.h>

/* just a bunch of shorter names for the common machine types */

typedef uintptr_t uptr_t;
typedef char chr_t;
typedef char32_t ucp_t;
typedef uint8_t u8_t;
typedef int8_t i8_t;
typedef uint16_t u16_t;
typedef int16_t i16_t;
typedef uint32_t u32_t;
typedef int32_t i32_t;
typedef uint64_t u64_t;
typedef int64_t i64_t;
typedef float f32_t;
typedef long double f64_t;

/* end rctypes.h */
#endif
